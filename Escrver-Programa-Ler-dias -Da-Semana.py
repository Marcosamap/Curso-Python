dia_semana = int(input(' Digite um número entre um e sete '))
dia = dia_semana
if dia == 1:
    print('Domingo ')
elif dia == 2:
    print(' Segunda ')
elif dia == 3:
    print(' Terça Feira ')
elif dia == 4:
    print(' Quarta feira ')
elif dia == 5:
    print(' Quinta feira ')
elif dia == 6:
    print(' Sexta feira ')
elif dia == 7:
    print(' Sábado ')
else:
    print(' Número não valido ')